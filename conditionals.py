# the if statement
# for the if statement, the indented code represents the block of code for the if statement
temperature = 35

if temperature > 30:
    print("Its a hot day today")
    print("Dont go out")


#using the else if conditional
# the else if conditonal is written as elif in python, see example below

weekday = input("What is today? ")

if weekday == "Monday":
    print("Thank God its monday")

elif weekday == "Tuesday":
    print("Its tuesday")

else:
    print("Its a free day today")
